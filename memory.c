#include "memory.h"
#include "stdlib.h"
#include "stddef.h"
#include "stdint.h"
#include "string.h"

static const uint8_t chip8_font[0x50] = {
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

static const int sprite_address_lookup[0x10] = {0, 5, 10, 15, 20,
                                                25, 30, 35, 40, 45,
                                                50, 55, 60, 65, 70, 75};

int memory_init(uint8_t **memory, int size)
{
    *memory = malloc(sizeof(uint8_t) * size);
    memcpy(*memory, chip8_font, 80);
    return *memory != NULL;
}

uint8_t memory_read(uint8_t *memory, int address)
{
    return memory[address];
}

void memory_write(uint8_t *memory, uint16_t address, uint8_t value)
{
    memory[address] = value;
}

void memory_free(uint8_t **memory)
{
    free(*memory);
    memory = NULL;
}

int get_sprite_address(int sprite)
{
    return sprite_address_lookup[sprite];
}
